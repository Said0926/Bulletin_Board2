class Command(BaseCommand):
    help = "Fills the database with 20 sample ads for development"

    def add_arguments(self, parser):
        parser.add_argument(
            "--clear",
            action="store_true",
            help="Delete all existing ads before seeding",
        )

    def handle(self, *args, **options) -> None:
        if options["clear"]:
            deleted, _ = Ad.objects.all().delete()
            self.stdout.write(self.style.WARNING(f"Удалено объявлений: {deleted}"))

        # Получаем или создаём тестового автора
        author, created = User.objects.get_or_create(
            email="demo@bulletinboard.local",
            defaults={
                "is_email_verified": True,
                "is_active": True,
                "first_name": "Demo",
                "last_name": "User",
            },
        )
        if created:
            author.set_password("demo12345")
            author.save()
            self.stdout.write(self.style.SUCCESS("Создан тестовый пользователь: demo@bulletinboard.local / demo12345"))

        created_count = 0
        for data in SAMPLE_ADS:
            Ad.objects.create(
                author=author,
                title=data["title"],
                content=data["content"],
                category=data["category"],
            )
            created_count += 1

        self.stdout.write(self.style.SUCCESS(f"Создано объявлений: {created_count}"))
